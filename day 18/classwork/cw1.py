# მოხმარებელს შემოატანინეთ თავისი გამოცდის ქულა, შემდეგ პირობითი განხცადების საშვალებით შეამოწმეთ ეს ქულა მეტია თუ 50-ზე, თუ მეტია დაუბეჭდეთ რომ გამოცდა ჩააბარა
score=int(input("შეიყვანე შენი გამოცდის ქულა:"))
if score>=50:
    print("შენ ჩააბარე გამოცდა!")
else:
    print("შენ ვერ ჩააბარე გამოცდა:")
# დამატებით გამოვიყენე else პირობითი ოპერაცია.
if score % 2 ==0:
    print("ჩადი ხარ:")
else:
    print("ტორტი ხარ")
    