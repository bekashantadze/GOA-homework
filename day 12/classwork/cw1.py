# სამივე ლოგიკურ ოპერატორზე: not, or, and: ჩამოწერეთ ყველა შესაძლო ვარიანტი და დაბეჭდეთ შედეგი გვერდზე კომენტარის საშვალებით მიწუერეთ შედეგი
# not operator
print(not True) 
# false
print( not False)
# true

# or operator
print(True or True)
# true
print(True or False)
# true
print(False or True)
# true
print(False or False)
# false

# and operator
print(True and True)
# true
print(True and False)
# false
print(False and True)
# false
print(False and False)
# false