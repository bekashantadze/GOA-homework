# გააკეთეთ 5-5 მაგალითი თთოეულ შედარების ოპერატორზე (>, >=, <, <=, ==, !=), გვერდზე კომენტარის საშვალებით მიუთითეთ თუ რომელ boolean მნიშვნელობას გამოიტანას, აიღეთ მრავალფეროვანი კომბინაციები, შეეცადეთ გქონდეთ ყველა ვარიანტი
# > (მეტობა)
print(10 > 5)  
# True
print(-3 > -5)  
# True
print(7.5 > 7.9)  
# False
print(89.8 > 78)
# true
print(78,9 > 6,8)
# true

# >= (მეტია ან ტოლია)
print(5 >= 5) 
 # True
print(100 >= 99)  
# True
print(-1 >= 0)  
# False
print(23,10 >= 9,0)
# true
print(-12,90 >= 12)
# false

# < (ნაკლებია)
print(2 < 10) 
 # True
print(0 < -1)  
# False
print(3.99 < 4.00)  
# True
print(100 < 90)
# false
print(89<45)
# false


# <= (ნაკლები ან ტოლი)
print(7 <= 7)  
# True
print(-3 <= -3)  
# True
print(5.5 <= 5.4)  
# False
print(70 <= 67)
# false
print(70 <= 78)
# true


# == (ტოლობა)
print(4 == 4) 
 # True
print(3.5 == 3.5) 
 # True
print(5 == 5)  
# False
print(90 == 90)
# true
print(89==90)
# false

# != (არ არის ტოლი)
print(10 != 5)  
# True
print(-1 != -1) 
 # False
print(3.14 != 2.71) 
 # True
print(15 != 7)
# true
print(78 != 78)
# false

