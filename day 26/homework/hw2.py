# 4) ახსენით კომენტარებით როგორ მუშაობს: lower, upper, capitalize მეთოდები
# lower() მეთოდი:
# გარდაქმნის მთელ სტრიქონს პატარა ასოებად
# მაგ: "HeLLo" -> "hello"

# upper() მეთოდი:
# გარდაქმნის მთელ სტრიქონს დიდ ასოებად
# მაგ: "HeLLo" -> "HELLO"

# capitalize() მეთოდი:
# სტრიქონის მხოლოდ პირველ ასოს აქცევს დიდად,
# ხოლო დანარჩენ ასოებს – პატარა ასოებად
# მაგ: "hELLO wORLD" -> "Hello world"
